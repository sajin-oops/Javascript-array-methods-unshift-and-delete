//ARRAY METHODS
let array = ['a','b','c','d','e']
//PUSH - ADD ELEMENT TO THE END
console.log(array.push('f'))
console.log(array)
console.log(array.pop())
console.log(array)
console.log(array.shift())
console.log(array)

//unshift - adds element to the start of the array
// and return new length
console.log(array.unshift('a'))
console.log(array)


//delete
delete array[1]
